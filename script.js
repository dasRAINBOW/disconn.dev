let STATE = {
  anim: false,
  activeScGame: false,
  activeSpin: false,
  allowAudioListener: true,
  allowTitleChange: true,
  audioTracker: null,
  isMobile: null,
  intervals: { anim: null, excited: null, gameLoop: null },
  spin: {
    counter: 0,
    randomAudio: Math.floor(Math.random() * CONFIG.audioIntro.length),
    randomCode: function () {
      let i = Math.floor(Math.random() * CONFIG.codes.length);
      if (CONFIG.codes[i] == this.selectedCode) {
        if (Math.round(Math.random()) == 0) {
          i += Math.ceil(Math.random() * (CONFIG.codes.length - i));
          return CONFIG.codes[i];
        } else {
          i -= Math.ceil(Math.random() * i);
          return CONFIG.codes[i];
        }
      } else if (CONFIG.codes[i] != this.selectedCode) {
        return CONFIG.codes[i];
      }
    },
    selectedCode: undefined,
  },
  sc: {
    gameField: "-",
    cursor: "I",
    range: "|---|",
    timer: 150,
    score: 0,
    fouls: 0,
    stickyScore: 0,
    size: 20,
    direction: true,
    moveCounter: 0,
    wouldScore: false,
    cyclePassed: true,
    randomPos: undefined,
    genRandomPos: function (lastPos) {
      let i = Math.round(Math.random() * STATE.sc.size);
      if (lastPos === undefined && Math.round(Math.random()) == 0) {
        lastPos = i;
      }
      if (i == lastPos) {
        if (Math.round(Math.random()) == 0) {
          i += Math.ceil(Math.random() * (STATE.sc.size - lastPos));
          return i;
        } else {
          i -= Math.ceil(Math.random() * lastPos);
          return i;
        }
      } else if (i != lastPos) {
        return i;
      }
    },
  },
};

