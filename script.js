const UpdateDifficulty = () => {
  if (
    STATE.sc.score in CONFIG.levels[STATE.isMobile ? "mobile" : "desktop"] &&
    STATE.sc.stickyScore < STATE.sc.score
  ) {
    STATE.sc.stickyScore = STATE.sc.score;
    for (i in CONFIG.levels[STATE.isMobile ? "mobile" : "desktop"][
      STATE.sc.stickyScore
    ]) {
      STATE.sc[i] =
        CONFIG.levels[STATE.isMobile ? "mobile" : "desktop"][
          STATE.sc.stickyScore
        ][i];
    }
    clearInterval(STATE.intervals.gameLoop);
    if (STATE.sc.moveCounter > STATE.sc.size) {
      STATE.sc.moveCounter = STATE.sc.size;
    }
    STATE.intervals.gameLoop = setInterval(gameLoop, STATE.sc.timer);
  }
};

window.addEventListener("load", () => {
  animTitle(CONFIG.defaulTitle, true);
  if (window.innerWidth < window.innerHeight) {
    STATE.isMobile = true;
  } else if (window.innerWidth > window.innerHeight) {
    STATE.isMobile = false;
  }
  DOM.titleBtn.addEventListener("click", () => {
    animToggle(true);
  });
  DOM.slotsText.addEventListener("animationend", decreaseSpin);
  DOM.scBtn.addEventListener("click", scBtnLogic);
  DOM.based.addEventListener("mouseenter", () => {
    STATE.audioTracker = playBased(true);
  });
  DOM.based.addEventListener("mouseleave", () => {
    playBased(false, STATE.audioTracker);
  });
  DOM.slotsBtn.addEventListener("mouseenter", () => {
    excitedAnimToggle(true);
  });
  DOM.slotsBtn.addEventListener("mouseleave", () => {
    excitedAnimToggle(false);
  });
  DOM.slotsBtn.addEventListener("click", () => {
    excitedAnimToggle(false);
    toggleSpin();
  });
  window.addEventListener("resize", () => {
    if (window.innerWidth < window.innerHeight) {
      STATE.isMobile = true;
    } else if (window.innerWidth > window.innerHeight) {
      STATE.isMobile = false;
    }
  });
  for (let i = 0; i < cookies.length; i++) {
    let splitCookie = cookies[i].split("=");
    cookiesObj[splitCookie[0]] = splitCookie[1];
  }
  if (cookiesObj.highScore !== undefined) {
    DOM.scHighScore.innerHTML = `Current highscore: ${cookiesObj.highScore}`;
  }
});

function animTitle(currentAnim, single, titleAnimSpeed = 250) {
  let i = 0;
  let animInterval = setInterval(() => {
    if (i < currentAnim.length - 1) {
      document.title = currentAnim[i];
      i++;
    } else {
      document.title = currentAnim[i];
      i = 0;
      if (single) {
        clearInterval(animInterval);
      }
    }
  }, titleAnimSpeed);
  return animInterval;
}

function animToggle(reload) {
  if (!STATE.anim && STATE.allowTitleChange) {
    STATE.anim = true;
    STATE.intervals.anim = animTitle(
      CONFIG.titleAnims[Math.floor(Math.random() * CONFIG.titleAnims.length)],
      false,
    );
  } else if (STATE.anim) {
    clearInterval(STATE.intervals.anim);
    if (reload) {
      animTitle(CONFIG.defaulTitle, true);
    }
    STATE.anim = false;
  }
}

function excitedAnimToggle(run) {
  if (run) {
    STATE.intervals.excited = setInterval(() => {
      DOM.slotsBtn.style.transform = `translate(${Math.round(Math.random() * 4 - 2).toString() + "%"}, ${Math.round(Math.random() * 6 - 3).toString() + "%"})`;
    }, 50);
  } else if (!run) {
    clearInterval(STATE.intervals.excited);
    DOM.slotsBtn.style.transform = ``;
  }
}

function toggleSpin() {
  if (!STATE.activeSpin) {
    STATE.activeSpin = true;
    DOM.slotsText.style.animation = ``;
    void DOM.slotsText.offsetWidth;
    let spinAudio = new Audio(CONFIG.audioIntro[STATE.spin.randomAudio]);
    spinAudio.play();
    DOM.slotsText.innerHTML = `${STATE.spin.randomCode()}`;
    DOM.slotsText.style.animationName = `spinAnim`;
    DOM.slotsText.style.animationDuration = `500ms`;
    DOM.slotsText.style.animationTimingFunction = `linear`;
    setTimeout(() => {
      DOM.slotsText.innerHTML = `${STATE.spin.randomCode()}`;
    }, 250);
  }
}

function decreaseSpin() {
  if (STATE.activeSpin) {
    DOM.slotsText.style.animation = ``;
    void DOM.slotsText.offsetWidth;
    if (STATE.spin.counter < CONFIG.spinSteps.length) {
      DOM.slotsText.style.animationName = `spinAnim`;
      DOM.slotsText.style.animationDuration = `${CONFIG.spinSteps[STATE.spin.counter].toString()}ms`;
      DOM.slotsText.style.animationTimingFunction = `linear`;
      setTimeout(() => {
        STATE.spin.selectedCode = STATE.spin.randomCode();
        DOM.slotsText.innerHTML = STATE.spin.selectedCode;
      }, CONFIG.spinSteps[STATE.spin.counter] / 2);
      STATE.spin.counter++;
    } else {
      STATE.spin.counter = 0;
      let spinAudio = new Audio(CONFIG.audioOutro[STATE.spin.randomAudio]);
      spinAudio.addEventListener("ended", () => {
        window.location.replace("https://youtu.be/" + STATE.spin.selectedCode);
      });
      spinAudio.play();
      STATE.activeSpin = false;
    }
  }
}

function playBased(run, basedAudio) {
  if (run) {
    basedAudio = new Audio("./media/audio/based.mp3");
    basedAudio.play();
    return basedAudio;
  } else if (!run && basedAudio !== undefined) {
    basedAudio.pause();
    basedAudio.currentTime = 0;
  }
}

function gameLoop() {
  let splitField = [];
  let doneField;

  if (STATE.sc.direction) {
    STATE.sc.moveCounter++;
    if (STATE.sc.moveCounter == STATE.sc.size + STATE.sc.range.length - 1) {
      STATE.sc.direction = false;
    }
  } else if (!STATE.sc.direction) {
    STATE.sc.moveCounter--;
    if (STATE.sc.moveCounter == 0) {
      STATE.sc.direction = true;
    }
  }

  splitField[0] =
    STATE.sc.gameField.repeat(STATE.sc.randomPos) +
    STATE.sc.range +
    STATE.sc.gameField.repeat(STATE.sc.size - STATE.sc.randomPos);
  splitField[1] = splitField[0].slice(0, STATE.sc.moveCounter);
  splitField[2] = splitField[0].slice(STATE.sc.moveCounter + 1);

  let isInside =
    STATE.sc.moveCounter > STATE.sc.randomPos &&
    STATE.sc.moveCounter < STATE.sc.randomPos + STATE.sc.range.length;

  STATE.sc.wouldScore = isInside;
  DOM.scBtn.setAttribute("green", `${isInside}`);

  doneField = splitField[1] + STATE.sc.cursor + splitField[2];
  DOM.scText.innerHTML = doneField;
  document.title = `${doneField} (${STATE.sc.score})`;
  if (!STATE.sc.cyclePassed) {
    STATE.sc.cyclePassed = true;
  }
}

function scBtnLogic() {
  if (!STATE.activeScGame && !STATE.activeSpin) {
    STATE.allowTitleChange = false;
    STATE.activeScGame = true;
    STATE.sc.score = 0;
    STATE.sc.stickyScore = 0;
    STATE.sc.fouls = 0;

    if (STATE.anim) {
      animToggle(false);
    }

    if (STATE.isMobile) {
      STATE.sc.size = 15;
    }

    DOM.scText.style.fontFamily = `Fira Code, monospace`;
    DOM.scBtn.innerHTML = `Score: ${STATE.sc.score} Lives: ${3 - STATE.sc.fouls}`;
    STATE.sc.randomPos = STATE.sc.genRandomPos();
    STATE.intervals.gameLoop = setInterval(gameLoop, STATE.sc.timer);
  } else if (
    STATE.activeScGame &&
    STATE.sc.wouldScore &&
    STATE.sc.cyclePassed
  ) {
    STATE.sc.cyclePassed = false;
    STATE.sc.score++;
    DOM.scBtn.innerHTML = `Score: ${STATE.sc.score} Lives: ${3 - STATE.sc.fouls}`;
    UpdateDifficulty();
    STATE.sc.randomPos = STATE.sc.genRandomPos(STATE.sc.randomPos);
  } else if (
    STATE.activeScGame &&
    !STATE.sc.wouldScore &&
    STATE.sc.cyclePassed
  ) {
    if (STATE.sc.score > 0 && STATE.sc.fouls < 3) {
      STATE.sc.cyclePassed = false;
      STATE.sc.score--;
      STATE.sc.fouls++;
      DOM.scBtn.innerHTML = `Score: ${STATE.sc.score} Lives: ${3 - STATE.sc.fouls}`;
      STATE.sc.randomPos = STATE.sc.genRandomPos(STATE.sc.randomPos);
    } else {
      clearInterval(STATE.intervals.gameLoop);
      if (STATE.sc.score > cookiesObj.highScore || cookiesObj.highScore === undefined) {
        cookiesObj.highScore = STATE.sc.score;
		document.cookie = `highScore=${cookiesObj.highScore};`;
		DOM.scHighScore.innerHTML = `Current highscore: ${cookiesObj.highScore}`;
      }
      animTitle(CONFIG.defaulTitle, true);
      DOM.scText.style.fontFamily = "monospace";
      DOM.scText.innerHTML = "so close :(";
      DOM.scBtn.innerHTML = "try again?";
      STATE.allowTitleChange = true;
      STATE.activeScGame = false;
    }
  }
}
